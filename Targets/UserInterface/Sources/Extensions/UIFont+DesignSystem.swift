import UIKit

public extension UIFont {
    
    static let headerB = UIFont.systemFont(ofSize: 21, weight: .bold)
    static let headerSB = UIFont.systemFont(ofSize: 21, weight: .semibold)
    static let headerR = UIFont.systemFont(ofSize: 21, weight: .regular)
    
    static let bodyB = UIFont.systemFont(ofSize: 17, weight: .bold)
    static let bodySB = UIFont.systemFont(ofSize: 17, weight: .semibold)
    static let bodyR = UIFont.systemFont(ofSize: 17, weight: .regular)
    
    static let captionB = UIFont.systemFont(ofSize: 14, weight: .bold)
    static let captionSB = UIFont.systemFont(ofSize: 14, weight: .semibold)
    static let captionR = UIFont.systemFont(ofSize: 14, weight: .regular)
    
}
